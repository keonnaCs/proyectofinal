        <h2>¿Qué es el machismo?</h2>
        <div class="columns">
            <div class="column">
                <h3>Definición de Machismo</h3>
                <p>El machismo es un sistema de creencias que promueve la supremacía del hombre sobre la mujer. Está arraigado en muchos aspectos de las culturas latinoamericanas y se manifiesta en la dominación de los hombres en el trabajo, la política y la familia. Los hombres machistas son los proveedores, protectores y tomadores de decisiones, mientras que las mujeres son a menudo tienen roles de subordinación.</p>
