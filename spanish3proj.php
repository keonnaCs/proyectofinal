    <section id="conclusion">
        <h2>Conclusión</h2>
        <p>Marianismo y machismo son dos conceptos profundamente interrelacionados que reflejan la desigualdad de género en muchas sociedades latinoamericanas. Ambos refuerzan los roles tradicionales de género, están limitando las oportunidades y la autonomía de las mujeres. Aunque ambos conceptos han sido criticados y desafiados en años recientes, continúan influyendo en las estructuras sociales y familiares de muchas comunidades.</p>
